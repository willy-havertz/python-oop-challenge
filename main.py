from pet import Pet

# Create an instance of the Pet class
my_pet = Pet("Simba")

# Interact with the pet
my_pet.get_status()
my_pet.eat()
my_pet.sleep()
my_pet.play()
my_pet.get_status()

# Train the pet and show its tricks
my_pet.train("Sit")
my_pet.train("Roll over")
my_pet.train("Shake hands")
my_pet.train("Play dead")
my_pet.train("Jump")
my_pet.train("Fetch")

my_pet.show_tricks()
